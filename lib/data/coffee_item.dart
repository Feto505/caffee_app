class CoffeeItem {
  final String name;
  final String subTitle;
  final String description;
  final String imagePath;
  final double rate;
  final double price;

  CoffeeItem({
    required this.name,
    required this.subTitle,
    required this.description,
    required this.imagePath,
    required this.price,
    required this.rate,
  });
}
